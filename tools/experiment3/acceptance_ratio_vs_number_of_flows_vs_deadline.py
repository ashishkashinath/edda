# Read CSV data
from io import StringIO
df = pd.read_csv(StringIO(data))

# Convert deadline to numeric (microseconds)
def parse_deadline(deadline_str):
    if deadline_str.endswith('us'):
        return float(deadline_str[:-2])
    elif deadline_str.endswith('ms'):
        return float(deadline_str[:-2]) * 1000
    return float(deadline_str)

df['Deadline_us'] = df['Deadline'].apply(parse_deadline)

# Get unique values for flows and deadlines
flows = df['NumberOfFlows'].unique()
deadlines = df['Deadline_us'].unique()

# Create meshgrid
X, Y = np.meshgrid(flows, deadlines)

# Create Z matrices for DCT and RealFlow
Z_DCT = np.zeros_like(X, dtype=float)
Z_RealFlow = np.zeros_like(X, dtype=float)

for i, deadline in enumerate(deadlines):
    for j, flow in enumerate(flows):
        mask = (df['NumberOfFlows'] == flow) & (df['Deadline_us'] == deadline)
        if mask.any():
            Z_DCT[i, j] = df.loc[mask, 'DCT'].values[0]
            Z_RealFlow[i, j] = df.loc[mask, 'RealFlow'].values[0]

# Create 3D plot
fig = plt.figure(figsize=(16, 9))
ax = fig.add_subplot(111, projection='3d')

# Adjust subplot position to make room for labels
fig.subplots_adjust(left=0.05, right=0.99, top=0.95, bottom=0.01)

# Plot surfaces with ColorBrewer sequential schemes
surf1 = ax.plot_surface(X, Y, Z_DCT, alpha=0.7, cmap='BuGn', 
                        label='X', edgecolor='none')
surf2 = ax.plot_surface(X, Y, Z_RealFlow, alpha=0.7, cmap='OrRd', 
                        label='RealFlow', edgecolor='none')

# Labels and title
ax.set_xlabel('Number of Flows', fontsize=24, labelpad=15)
ax.set_ylabel('Deadline (μs)', fontsize=24, labelpad=15)
ax.set_zlabel('Acceptance Ratio', fontsize=24, labelpad=10)
ax.tick_params(axis='both', which='major', labelsize=20)
ax.tick_params(axis='both', which='minor', labelsize=20)

cbar_x = fig.colorbar(surf1, ax=ax, shrink=0.5, aspect=20, orientation='horizontal', pad=0.03)
cbar_rf = fig.colorbar(surf2, ax=ax, shrink=0.5, aspect=20, orientation='horizontal', pad=0.05)

cbar_x.ax.tick_params(labelsize=18)
cbar_rf.ax.tick_params(labelsize=18)
cbar_x.set_label(label='Edda', size=20)
cbar_rf.set_label(label='RealFlow', size=20)

# Set viewing angle
ax.view_init(elev=15, azim=35)

# Add grid
ax.grid(True, alpha=0.3)

plt.savefig("acceptance_ratio_vs_number_of_flows_vs_deadline.pdf", dpi=300, bbox_inches="tight")
plt.close()